/* 

Задание 5.
Написать скрипт, который при открытии страницы будет делать следующее:
Если пользователь зашел в первый раз, вывести окно prompt с сообщением: «Добро пожаловать! Назовите, пожалуйста, ваше имя».
После того, как пользователь введет имя, записать имя, дату и время визита в localStorage.
Подсказка: для определения текущей даты используйте new Date().
Если пользователь открывает страницу не впервые (это можно узнать по наличию соответствующих записей в localStorage), вывести в alert
сообщение вида: «Добрый день, *имя пользователя*! Давно не виделись. В последний раз вы были у нас *дата последнего посещения*» 
и перезаписать дату последнего посещения.
Дату можно вывести в любом удобочитаемом формате (не Timestamp, должен четко читаться день, месяц, год и время — часы и минуты). */

// Получение данных в localStorage
let myKey = localStorage.getItem('tstKey');

// Если localStorage пуст
if (myKey === null) {
	var usrName = prompt(`Добро пожаловать! Назовите, пожалуйста, ваше имя`);
	var curDate = new Date();

	//JSON для записи
	const jsonString = `
  {
    "name": ${usrName},
    "date": ${curDate}
  }`;

	//Запись в localStorage
	localStorage.setItem('tstKey', jsonString);
} else {
	alert(`Добрый день, ${myKey.name}! Давно не виделись. В последний раз вы были у нас ${myKey.date}`);

	//JSON для перезаписи
	const jsonString = `
  {
    "name": ${myKey.name},
    "date": ${new Date()}
  }`;

	//Перезапись localStorage
	localStorage.setItem('tstKey', jsonString);
}